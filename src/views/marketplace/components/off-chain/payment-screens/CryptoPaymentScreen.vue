<template>
<b-card-text class="action-section">
  <div class="" v-if="timedOutOrExpired || expired">
    <div class="d-flex justify-content-center">
      <p class="mt-4 text-center text-message">Invoice has expired</p>
    </div>
    <div class="d-flex justify-content-center">
      <b-button class="btn-pay" @click="$emit('rpayEvent', { opcode: 'crypto-payment-expired' })" variant="primary">Start Over</b-button>
    </div>
  </div>
  <div class="" v-else>
    <p v-if="!checkingChain && paymentOption === 'bitcoin' || paymentOption === 'lightning'" class="mt-4 text-center text-message">Scan the QR code <b-icon icon="arrow-right"/> <a v-if="paymentOption === 'lightning' || paymentOption === 'bitcoin'" href="#" class="text-payments" @click.prevent="doCheckChain()">check payment</a></p>
    <p v-if="checkingChain" class="mt-4 text-center text-message"><b-icon icon="circle" animation="throb"/> Checking for paid invoice</p>
    <p class="mt-4 text-center text-message text-center" v-if="paymentOption === 'fiat'">Enter your card details</p>
    <div class="d-flex justify-content-center">
      <SquareWebFormPaymentScreen :transactionData="transactionData" :configuration="configuration" v-on="$listeners" v-if="paymentOption === 'fiat'"/>
      <LightningPaymentAddress :configuration="configuration" @rpayEvent="handleEvent" v-if="paymentOption === 'lightning'"/>
      <BitcoinPaymentAddress :configuration="configuration" @rpayEvent="handleEvent" v-if="paymentOption === 'bitcoin'"/>
      <StacksPaymentAddress :configuration="configuration" v-on="$listeners" :desktopWalletSupported="desktopWalletSupported" v-if="paymentOption === 'stacks'"/>
      <EthereumPaymentAddress :configuration="configuration" v-on="$listeners" :desktopWalletSupported="desktopWalletSupported" v-if="paymentOption === 'ethereum'"/>
    </div>
  </div>
</b-card-text>
</template>

<script>
import { APP_CONSTANTS } from '@/app-constants'
import LightningPaymentAddress from './components/LightningPaymentAddress'
import BitcoinPaymentAddress from './components/BitcoinPaymentAddress'
import StacksPaymentAddress from './components/StacksPaymentAddress'
import EthereumPaymentAddress from './components/EthereumPaymentAddress'
import SquareWebFormPaymentScreen from './SquareWebFormPaymentScreen'

export default {
  name: 'CryptoPaymentScreen',
  components: {
    LightningPaymentAddress,
    BitcoinPaymentAddress,
    EthereumPaymentAddress,
    StacksPaymentAddress,
    SquareWebFormPaymentScreen
  },
  props: ['configuration', 'transactionData'],
  data () {
    return {
      expired: false,
      checkingChain: false,
      message: null,
      paying: false,
      loading: true,
      myChainChecker: null
    }
  },
  mounted () {
    this.checkChain()
    this.loading = false
  },
  beforeDestroy () {
    clearInterval(this.myChainChecker)
  },
  methods: {
    handleEvent (data) {
      if (data.opcode === 'crypto-payment-expired') {
        this.expired = true
      } else {
        this.$emit('rpayEvent', data)
      }
    },
    checkChain () {
      const $self = this
      this.myChainChecker = setInterval(function () {
        // $self.doCheckChain()
        $self.expired = $self.$store.getters[APP_CONSTANTS.KEY_INVOICE_EXPIRED]
      }, 2000)
    },
    doCheckChain () {
      this.checkingChain = true
      const invoice = this.$store.getters[APP_CONSTANTS.KEY_INVOICE]
      this.$store.dispatch('merchantStore/checkPayment', invoice.id).then((invoice) => {
        const expired = this.$store.getters[APP_CONSTANTS.KEY_INVOICE_EXPIRED]
        this.checkingChain = false
        if (invoice && invoice.opcode === 'btc-crypto-payment-success') {
          this.$store.commit('merchantStore/setDisplayCard', 104)
          this.$notify({ type: 'success', title: 'Payment Detected', text: 'Redirecting to NFT transfer' })
        } else {
          this.$notify({ type: 'warning', title: 'Payment Not Detected', text: 'Scan the qr code or paste the info into your btc wallet.' })
        }
        return expired
      })
    }
  },
  computed: {
    desktopWalletSupported () {
      const paymentOption = this.configuration.payment.paymentOption
      return paymentOption === 'bitcoin' || paymentOption === 'lightning' || paymentOption === 'stacks'
    },
    paymentOption () {
      const paymentOption = this.configuration.payment.paymentOption
      return paymentOption
    },
    timedOutOrExpired () {
      const expired = this.$store.getters[APP_CONSTANTS.KEY_INVOICE_EXPIRED]
      return expired
    }
  }
}
</script>
